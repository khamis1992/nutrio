/// Extension methods for convenient theme access
extension ThemeExtensions on ThemeData {
  Color get primaryContainer => colorScheme.primaryContainer;
  Color get secondaryContainer => colorScheme.secondaryContainer;
  Color get surfaceVariant => colorScheme.surfaceContainerHighest;
  Color get outline => colorScheme.outline;

  // Additional health-focused color getters
  Color get mintPrimary => AppTheme.primaryLight;
  Color get motivationalOrange => AppTheme.motivationalColor;
  Color get healthyBackground => AppTheme.backgroundLight;
  Color get cleanCard => AppTheme.cardLight;
}

