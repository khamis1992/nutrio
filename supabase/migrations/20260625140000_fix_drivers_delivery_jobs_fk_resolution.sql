-- 2a. drivers.user_id → auth.users(id)
-- The inline FK exists but PostgREST needs a named constraint it can detect.
-- Drop the inline constraint and recreate as named.
DO $$
DECLARE
  v_constraint_name TEXT;
BEGIN
  -- Find the existing FK constraint name (auto-generated by inline REFERENCES)
  SELECT con.conname INTO v_constraint_name
  FROM pg_constraint con
  JOIN pg_class rel ON rel.oid = con.conrelid
  WHERE rel.relname = 'drivers'
    AND con.contype = 'f'
    AND con.confrelid = 'auth.users'::regclass;

  IF v_constraint_name IS NOT NULL AND v_constraint_name != 'fk_drivers_user_id' THEN
    -- Drop the auto-generated constraint
    EXECUTE format('ALTER TABLE public.drivers DROP CONSTRAINT %I', v_constraint_name);
  END IF;

  -- Create named constraint if it doesn't exist
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'fk_drivers_user_id'
  ) THEN
    ALTER TABLE public.drivers
    ADD CONSTRAINT fk_drivers_user_id
    FOREIGN KEY (user_id) REFERENCES auth.users(id) ON DELETE CASCADE;
  END IF;
END$$;

