-- is_active: used by getOnlineDrivers() filter (is_active=eq.true)
-- The table has is_online but code queries is_active
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'drivers' AND column_name = 'is_active'
  ) THEN
    ALTER TABLE public.drivers ADD COLUMN is_active BOOLEAN NOT NULL DEFAULT true;
    -- Sync existing data: is_active mirrors is_online
    UPDATE public.drivers SET is_active = is_online;
  END IF;
END$$;

